const mongoose = require("mongoose");
const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
    min: 0,
  },
  category: {
    type: String,
    lowercase: true,
    enum: ["fruit", "vegetable", "dairy"],
  },
});
const Product = mongoose.model("Product", productSchema);
//the name of collection in mongodb will be lowercase pluralsed Product so collection is "products"
module.exports = Product;
